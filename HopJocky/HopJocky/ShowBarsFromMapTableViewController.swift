import UIKit
import Firebase

class ShowBarsFromMapTableViewController: UITableViewController {
    
    var bars = [Bar]()
    
    override func viewDidLoad() {
        super.viewDidLoad()

        //be sure that the tableView updates when there is new data
        self.tableView.reloadData()
        
        //order bars by distance
        for bar in bars{
            bar.setDistanceFromUsr()
        }
        bars.sortInPlace({ $0.distanceFromUsr < $1.distanceFromUsr})
    }
    
    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
        // Dispose of any resources that can be recreated.
    }
    
    // MARK: - Table view data source
    
    override func numberOfSectionsInTableView(tableView: UITableView) -> Int {
        return 1
    }
    
    override func tableView(tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return bars.count
    }
    
    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("cellid", forIndexPath: indexPath)
        
        // Configure the cell...
        let bar = bars[indexPath.row]
        cell.textLabel?.text = bar.name
        cell.detailTextLabel?.text = String(format: "%.1f", bar.distanceFromUsr) + " m"
        
        return cell
    }
    
    
    // MARK: - Navigation
    
    // In a storyboard-based application, you will often want to do a little preparation before navigation
    override func prepareForSegue(segue: UIStoryboardSegue, sender: AnyObject?) {
        // Get the new view controller using segue.destinationViewController.
        // Pass the selected object to the new view controller.
        
        let indexPath = self.tableView.indexPathForSelectedRow
        
        let barDetailView = segue.destinationViewController as! BarDetailViewController
        
        barDetailView.barObj = bars[indexPath!.row]
    }
    
    

